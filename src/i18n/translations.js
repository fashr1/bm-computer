// t() สำหรับโค้ดนอก React (lib/*.js) - อ่านภาษาปัจจุบันจาก localStorage เอง
export function tOutside(path) {
  let lang = 'th'
  try { lang = localStorage.getItem('bm-lang') || 'th' } catch { /* SSR/ไม่มี storage */ }
  let cur = translations[lang]
  for (const p of path.split('.')) cur = cur?.[p]
  return cur ?? path
}
